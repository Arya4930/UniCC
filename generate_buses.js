function parseRaw(rawStr, type) {
    return rawStr.trim().split('\n').filter(l => l.trim()).map(line => {
        const [route, points] = line.split(':');
        return {
            id: type.toLowerCase() + '-' + route.trim().replace(/\s+/g, '-').toLowerCase(),
            type: type,
            route: route.trim(),
            boardingPoints: points.split(',').map(p => p.trim()),
            driverPhone: '',
            driverName: '',
            whatsappGroup: '',
            busLocation: ''
        };
    });
}

